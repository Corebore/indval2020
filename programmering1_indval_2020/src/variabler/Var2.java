package variabler;

public class Var2 {

	public static void main(String[] args) {


		double tal1; // deklarering av variabel av datatypen double(decimaltal)

		tal1=5.78; // tilldelar variabeln tal1 värdet 5.78


		double tal2= 10.5; // deklarering + tilldelning = initiering

		double summa = tal1+tal2 ; // deklarerar variabeln summa tilldelar den värde

		System.out.println(summa); // skriver ut summa


	}



}
